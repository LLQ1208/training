package com.acsm.training.web;

public class CferException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public CferException() {
		super();
	}

	public CferException(String message, Throwable cause) {
		super(message, cause);
	}

	public CferException(String message) {
		super(message);
	}

	public CferException(Throwable cause) {
		super(cause);
	}

	
}
